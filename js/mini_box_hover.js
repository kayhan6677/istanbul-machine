function slideInMachining() {
    document.getElementById("mini_machining").style.opacity = ("1");
    document.getElementById("mini_machining").style.animationName = ("getin");
    document.getElementById("mini_machining").style.animationDuration = ("0.3s");
    document.getElementById("mini_machining").style.animationTimingFunction = ("ease-out");
};

